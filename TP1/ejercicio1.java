public class OperadoresBasicos{
	public static void main(String[] args) {
		int a = 8;
		int b = 3;

		System.out.println("Valor de la suma de A y B: " + (a + b));
		System.out.println("Valor de la resta de A y B: " + (a - b));
		System.out.println("Valor de la multiplicacion de A y B: " + (a * b));
		System.out.println("Valor de la division de A y B: " + (a / b));
		System.out.println("Valor del resto de A y B: " + (a % b));
	}
}
